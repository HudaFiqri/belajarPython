#lower() digunakan untuk membuat tulisan menjadi huruf kecil

a = 'saya suka python'
b = 'Saya Suka Ngoding'
c = 'SAYA SUKA LINUX'


#tidak dengan huruf kapital
print('#tidak dengan huruf kapital')
print(a.lower())

print('')

#dengan huruf kapital di setiap kata
print('#dengan huruf kapital di setiap kata')
print(b.lower())

print('')

#dengan huruf kapital di semua kata
print('#dengan huruf kapital di semua kata')
print(c.lower())
