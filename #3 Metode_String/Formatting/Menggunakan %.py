#variable
text1 = 'linux'
angka1int = 100
angka2flo = 10.5

'''
NOTE*
%s - digunakan untuk String
%d - digunakan untuk Integer
%f - digunakan untuk Float

sumber referensi: https://www.learnpython.org/en/String_Formatting
'''

#dengan menggunakan % bisa memanggil kata yang berada pada variable yang akan dipanggil
print('saya menggunakan OS %s'%text1)

#menampilkan output angka integer
print('angka integer %d'%angka1int)

#menampilkan output angka float
print('angka float %f'%angka2flo)