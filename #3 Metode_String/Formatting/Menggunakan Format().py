#masukkan variablenya lalu tambahkan kurawal untuk membuat variablenya
#lalu panggil dengan format()
print('aku suka {barang}'.format(barang='pisang'))

#memanggil variable dengan array
print('aku suka {0} dan aku suka menngunakan OS {1} dan aku menggunakan {2}'.format('bawang', 'linux', 'python'))

#memanggil variabl dengan urutan
print('linux adalah {} sedangkan windows adalah {}'.format('penguin', 'jendela'))